    <p>
        <b> Actualizado: 30 de noviembre de 2020 <i style="color:red;">NO ES EL DEL CAUCHO</i></b>
        <br>
        A través de la Ley 1581 de 2012 y el Decreto 1377 de 2013, se desarrolla el derecho constitucional que tienen
        todas
        las personas a conocer, suprimir, actualizar y rectificar todo tipo de datos personales recolectados,
        almacenados o
        que hayan sido objeto de tratamiento en bases de datos en las entidades del públicas y privadas.
    </p>
    <p>
        La Corte Constitucional lo definió como el derecho que otorga la facultad al titular de datos personales de
        exigir
        de las administradoras de esos datos el acceso, inclusión, exclusión, corrección, adición, actualización y
        certificación de los datos, así como la limitación en las posibilidades de su divulgación, publicación o cesión,
        de
        conformidad con los principios que regulan el proceso de administración de datos personales. Asimismo, ha
        señalado
        que este derecho tiene una naturaleza autónoma que lo diferencia de otras garantías con las que está en
        permanente
        relación, como los derechos a la intimidad y a la información.
    </p>
    <p>
        En atención a la mencionada Ley se estableció el Registro Nacional de Bases de Datos - RNBD, el cual es un
        directorio público de las bases de datos con información personal sujetas a tratamiento que operan en el país,
        acorde con lo establecido en la Ley 1581 de 2012.

    </p>
    <p>
        Para consultar las bases de datos registradas por el Ministerio de Educación Nacional en el Registro Nacional de
        Bases de Datos - RNBD, ingrese aquí, y realice la búsqueda digitando Ministerio de Educación Nacional en el
        campo
        "Nombre o Razón Social" o el NIT 899999001 en "Documento de Identificación", ingrese el código CAPTCHA y
        finalice
        dando clic en el botón Buscar
